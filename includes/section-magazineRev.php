                            <h3 class="display-1 l-10 fg-brown">Informationsskærm</h3>
                            <h1 class="display-6">Målrettet indhold til dine medarbejdere</h1>
                            <div class="display-2 work-sans l-10">
                                Det interaktive infobord er en smuk løsning, der præsenterer budskabet flot og iøjnefaldende. Den vinklede skærm inviterer til interaktion. Skærmen er ideel til at præsentere praktisk information, gode historier, gennemgang af sikkerhedsinstruktioner, branding samt levering af beskeder med høje visuelle effekter i form af billeder og videoer.
        <br><br>
        <span class="f-w-6">Interaktion</span><br>
        Touch-funktionen muliggør, at der kan søges og vælges mellem flere lag af forskelligt indhold, så mangeartede informationer kan tilgås. Det interaktive Infobord er velegnet til at byde velkommen i et indgangsparti, eller på et strategisk sted i bygninger, hvor der er behov for at give målrettet interaktiv information.
        <br><br>
        <span class="f-w-6">Interaktion</span><br>
        Gennem designprocessen er der lagt vægt på holdbarhed, pålidelighed, god touch-funktion og nutidigt design. Brugervenlighed, nem vedligeholdelse og nem servicering af skærmen er ligeledes kendetegnende ved dette produkt.
